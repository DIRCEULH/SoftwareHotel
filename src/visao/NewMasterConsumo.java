    @SuppressWarnings("unchecked")
    private void refreshButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshButtonActionPerformed
        entityManager.getTransaction().rollback();
        entityManager.getTransaction().begin();
        java.util.Collection data = query.getResultList();
        for (Object entity : data) {
            entityManager.refresh(entity);
        }
        list.clear();
        list.addAll(data);
    }//GEN-LAST:event_refreshButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int[] selected = masterTable.getSelectedRows();
        List<modelagem.Consumo> toRemove = new ArrayList<modelagem.Consumo>(selected.length);
        for (int idx = 0; idx < selected.length; idx++) {
            modelagem.Consumo c = list.get(masterTable.convertRowIndexToModel(selected[idx]));
            toRemove.add(c);
            entityManager.remove(c);
             javax.swing.JOptionPane.showMessageDialog(null, "Para excluir selecione Salvar");
        }
        list.removeAll(toRemove);
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void newButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newButtonActionPerformed
        modelagem.Consumo c = new modelagem.Consumo();
        entityManager.persist(c);
        list.add(c);
        int row = list.size() - 1;
        masterTable.setRowSelectionInterval(row, row);
        masterTable.scrollRectToVisible(masterTable.getCellRect(row, 0, true));
    }//GEN-LAST:event_newButtonActionPerformed
    
    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
          if(nomeField.getText().equals("")){
    javax.swing.JOptionPane.showMessageDialog(null, "Nome campo obrigatório","",2);
    return;

}if(nomeprodutoField.getText().equals("")){
    javax.swing.JOptionPane.showMessageDialog(null, "Produto campo obrigatório","",2);
    return;
        }if(precoField.getText().equals("")){
    javax.swing.JOptionPane.showMessageDialog(null, "Preco campo obrigatório","",2);
    return;
    }if(quantidadeField.getText().equals("")){
    javax.swing.JOptionPane.showMessageDialog(null, "Quantidade campo obrigatório","",2);
    return;
     }if(diaField.getText().equals("")){
    javax.swing.JOptionPane.showMessageDialog(null, "Data campo obrigatório","",2);
     return;
     }if(anoField.getText().equals("")){
    javax.swing.JOptionPane.showMessageDialog(null, "Data campo obrigatório","",2);
       return;
       } if(anoField.getText().equals("")){
    javax.swing.JOptionPane.showMessageDialog(null, "Data campo obrigatório","",2);
       return;}
       
       try {
            entityManager.getTransaction().commit();
            entityManager.getTransaction().begin();
             javax.swing.JOptionPane.showMessageDialog(null, "Salvo com sucesso");
        } catch (RollbackException rex) {
            rex.printStackTrace();
            entityManager.getTransaction().begin();
            List<modelagem.Consumo> merged = new ArrayList<modelagem.Consumo>(list.size());
            for (modelagem.Consumo c : list) {
                merged.add(entityManager.merge(c));
            }
            list.clear();
            list.addAll(merged);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

